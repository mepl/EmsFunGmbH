package Objects;

public class Kunde 
{
	private int _kundenID;
	private String _name;
	private String _vorname;
	private String _strasse;
	private String _hausNummer;
	private String _plz;
	private String _ort;
	private String _telefon;
	private String _email;
	
	public int getKundenID() 
	{
		return _kundenID;
	}
	public void setKundenID(int _kundenID) 
	{
		this._kundenID = _kundenID;
	}
	public String getName() 
	{
		return _name;
	}
	public void setName(String _name) 
	{
		this._name = _name;
	}
	public String getVorname() 
	{
		return _vorname;
	}
	public void setVorname(String _vorname) 
	{
		this._vorname = _vorname;
	}
	public String getStrasse() 
	{
		return _strasse;
	}
	public void setStrasse(String _strasse) 
	{
		this._strasse = _strasse;
	}
	public String getHausNummer() 
	{
		return _hausNummer;
	}
	public void setHausNummer(String _hausNummer) 
	{
		this._hausNummer = _hausNummer;
	}
	public String getPlz() 
	{
		return _plz;
	}
	public void setPlz(String _plz) 
	{
		this._plz = _plz;
	}
	public String getOrt() 
	{
		return _ort;
	}
	public void setOrt(String _ort) 
	{
		this._ort = _ort;
	}
	public String getTelefon() 
	{
		return _telefon;
	}
	public void setTelefon(String _telefon) 
	{
		this._telefon = _telefon;
	}
	public String getEmail() 
	{
		return _email;
	}
	public void setEmail(String _email) 
	{
		this._email = _email;
	}

}
